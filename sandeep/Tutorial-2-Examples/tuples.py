#  Another important structures in Python are Tuples
#  Tuples are similar to lists, we can use them to store
#  a list of items but unlike lists we cannot modify them,
#  we cannot add or remove new items.
#  We say tuples are immutable
#  We use [] to define lists and () to define tuples

numbers = (3, 5, 7)
# numbers[0] = 10 - will get error "'tuple' object does not support item assignment"
print(numbers[0])
