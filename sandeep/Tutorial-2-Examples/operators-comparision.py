# Comparision operators are used when we have compare a variable with value
# Example : if temperature is greater than 30
#   it's a hot day
# otherwise if it's less than 10
#   it's a cold day
# otherwise
#   it's neither hot nor cold
# >, <, >=, <=, ==, != -- different operators

temperature = 35
if temperature > 30:
    print("it's a hot day")
else:
    print("it's not a hot day")
