print(10 + 3)  # Addition
print(10 - 3)  # Subtration
print(10 * 3)  # Mutliplication
print(10 / 3)  # Division results in floating point number
print(10 // 3)  # Division results in integer
print(10 % 3)  # Modulus remainder of a division
print(10 ** 3)  # exponent

